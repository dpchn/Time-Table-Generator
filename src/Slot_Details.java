    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
int i,count1=0,count2=0,count3=0;
for(i=0;i<m1.size();i++)
{
   if(m1.getElementAt(i).toString().equals(jComboBox1.getSelectedItem()))
    {
    count1++;
    }
if(m2.getElementAt(i).toString().equals(jComboBox2.getSelectedItem().toString()+":"+jComboBox4.getSelectedItem().toString()))
   {
       count2++;
   }
 if(m3.getElementAt(i).toString().equals(jComboBox5.getSelectedItem().toString()+":"+jComboBox3.getSelectedItem().toString()))
   {
       count3++;
   }

}


if(count1>0)
{
    jOptionPane1.showMessageDialog(this,"Slot no. Already entered");
}
else if(count2>0 || count3>0)
{
    jOptionPane1.showMessageDialog(this,"Slot already assigned to that time");
}
else if(jComboBox1.getSelectedIndex()==0)
{
    jOptionPane1.showMessageDialog(this,"Invalid Slot no. Selection ");
}
else if(jComboBox2.getSelectedIndex()==0 || jComboBox3.getSelectedIndex()==0)
{
    jOptionPane1.showMessageDialog(this,"Invalid Time Selection ");
}
else
{
        jList1.setModel(m1);
        jList2.setModel(m2);
        jList3.setModel(m3);

        String sn=jComboBox1.getSelectedItem().toString();
        String start=jComboBox2.getSelectedItem().toString()+":"+jComboBox4.getSelectedItem().toString();
        String end=jComboBox5.getSelectedItem().toString()+":"+jComboBox3.getSelectedItem().toString();

        jComboBox1.setSelectedItem("");
        jComboBox2.setSelectedItem("");
        jComboBox3.setSelectedItem("");
        jComboBox4.setSelectedItem("");
        jComboBox5.setSelectedItem("");

        m1.addElement(sn);
        m2.addElement(start);
        m3.addElement(end);
}
    }//GEN-LAST:event_jButton1ActionPerformed

