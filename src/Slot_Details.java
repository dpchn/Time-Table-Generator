    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int a=m3.getSize(),i;
        if(m1.isEmpty())
        {

        }
        else
        {

            try{
                String str="SELECT * FROM Slot_Details";
                Class.forName("sun.jdbc.odbc.JdbcOdbcDriver");
                Connection con=DriverManager.getConnection("jdbc:odbc:College");
                Statement stm=con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
                ResultSet r=stm.executeQuery(str);
                for(i=0;i<a;i++)
                {
                    String slot=m1.elementAt(i).toString();
                    String start=m2.elementAt(i).toString();
                    String end=m3.elementAt(i).toString();

                    r.next();
                    r.moveToInsertRow();
                    r.updateString("SLOT_NO",slot);
                    r.updateString("START", start);
                    r.updateString("END", end);
                    r.insertRow();
                }
                r.close();
                con.close();
                jOptionPane1.showMessageDialog(this,"Successfully Entered");
                m1.removeAllElements();
                m2.removeAllElements();
                m3.removeAllElements();
            }

            catch(Exception e)
            {

            }

        }
    }//GEN-LAST:event_jButton2ActionPerformed


